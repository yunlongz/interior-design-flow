-- ============================================================

-- 阶段表
CREATE TABLE IF NOT EXISTS phases (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL UNIQUE,
    sort_order INTEGER NOT NULL DEFAULT 0,
    created_at TEXT DEFAULT CURRENT_TIMESTAMP
);

-- 部门表
CREATE TABLE IF NOT EXISTS departments (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL UNIQUE,
    sort_order INTEGER NOT NULL DEFAULT 0,
    created_at TEXT DEFAULT CURRENT_TIMESTAMP
);

-- 节点表（核心）
CREATE TABLE IF NOT EXISTS nodes (
    id TEXT PRIMARY KEY,
    title TEXT NOT NULL,
    type TEXT NOT NULL CHECK(type IN ('task','review','deliverable','decision','milestone')),
    detail TEXT,
    phase_id INTEGER NOT NULL REFERENCES phases(id),
    dept_id INTEGER NOT NULL REFERENCES departments(id),
    sort_order INTEGER NOT NULL DEFAULT 0,
    created_at TEXT DEFAULT CURRENT_TIMESTAMP,
    updated_at TEXT DEFAULT CURRENT_TIMESTAMP
);

-- 节点连线关系表
CREATE TABLE IF NOT EXISTS connections (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    from_node TEXT NOT NULL REFERENCES nodes(id),
    to_node TEXT NOT NULL REFERENCES nodes(id),
    type TEXT NOT NULL DEFAULT '流程',
    description TEXT,
    created_at TEXT DEFAULT CURRENT_TIMESTAMP,
    UNIQUE(from_node, to_node)
);

-- 视图：方便按阶段+部门聚合查询
CREATE VIEW IF NOT EXISTS v_nodes_full AS
SELECT 
    n.id, n.title, n.type, n.detail, n.sort_order,
    p.id as phase_id, p.name as phase_name,
    d.id as dept_id, d.name as dept_name
FROM nodes n
JOIN phases p ON n.phase_id = p.id
JOIN departments d ON n.dept_id = d.id;

